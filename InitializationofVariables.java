package com.deocampo.initializationofvariables;

/**
 *
 * @author maris
 */
public class InitializationofVariables {

    public static void main(String[] args) {
       String lastname = "De Ocampo";
       String firstname = "Charles Richmond";
       char middleinitial = 'D';
       int age = 18;
       double height = 179.0;
       double weight = 65.5;
       
       System.out.println("Last name: " + lastname);
       System.out.println("First name: " + firstname);
       System.out.println("Middle Initial: " + middleinitial);
       System.out.println("age: " + age);
       System.out.println("Height in cm " + height + "cm");
       System.out.println("Weight in kg " + weight + "kg");
       
    }
}
